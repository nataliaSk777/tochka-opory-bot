function getMorningText(programType, day, supportStep) {
  if (programType === 'free') return FREE_MORNING[day] || null;
  if (programType === 'paid') return PAID_MORNING[day] || null;
  if (programType === 'support') {
    const idx = (Math.max(1, supportStep) - 1) % SUPPORT_MORNING.length;
    return SUPPORT_MORNING[idx];
  }
  return null;
}

