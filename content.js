// 2–3 раза в неделю: вечером добавляем хокку автоматически
const EVENING_HAIKU_DAYS = {
  free: new Set([2, 4, 6]),
  paid: new Set([9, 11, 13, 16, 18, 20, 23, 25, 27, 30, 32, 34])
};

function pickHaiku(programType, day) {
  const d = Number(day || 1);
  const idx = Math.abs(d - 1) % HAIKU.length;
  const h = HAIKU[idx];
  if (!h) return null;

  if (programType === 'free' && EVENING_HAIKU_DAYS.free.has(d)) return h;
  if (programType === 'paid' && EVENING_HAIKU_DAYS.paid.has(d)) return h;

  return null;
}

function withHaiku(programType, day, text) {
  if (!text) return null;
  const h = pickHaiku(programType, day);
  if (!h) return text;
  return `${text}\n\n${h}`;
}

